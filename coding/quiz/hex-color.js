var bodyText = "", currentHex, score = 0, hexInfo, bigDim, dim, inverse;

function submitCor() {
	var C0r = document.getElementById("cor").value;
	var cr = C0r.toLowerCase().replace(/[^a-z]+/g, "");
	currentHex = corDict[cr];
	if (currentHex === undefined) {
		window.alert(cr + " is not a standard HTML color name.");
	} else {
		hexInfo = hexDict[currentHex];
		if (hexInfo[0]===0) {
			hexDict[currentHex][0] = 1;
			score++;
			document.getElementById("score").innerHTML = score;
			bigDim = Math.max(Math.min(window.innerWidth - 10, window.innerHeight - 85), 50); 
			document.getElementById("body").style.width = bigDim + "px";
			dim = Math.floor((bigDim - 5) / Math.ceil(Math.sqrt(score)));
			document.getElementById("corBoxSpecs").innerHTML = ".corBox{display:inline-box;float:left;border-radius:5%;width:" + dim + "px;height:" + dim + "px;}";
			inverse = invertColor(currentHex);
			bodyText += "<div id="+currentHex+" class=\"corBox\" style=\"background-color:"+currentHex+";text-align:center;color:"+inverse+";\"><h5>"+currentHex.toLowerCase()+"</h5><h6>"+hexInfo[1]+"</h6></div>";
			document.getElementById("body").innerHTML = bodyText;
		} else {
			window.alert("You already have "+hexInfo[1]+".");
		};
	};
	document.getElementById("cor").value = "";
}

function invertColor(hexTripletColor) {
        var color = hexTripletColor;
		var RR = color.substring(1,3);
		var GG = color.substring(3,5);
		var BB = color.substring(5,7);
        //color = parseInt(color.substring(1),16); // remove # & convert to integer
        //color = 0xFFFFFF - color; // invert
        //color = color.toString(16); // convert to hex string
        //color = "#" + (("000000" + color).slice(-6)); // pad with leading zeros
		color = "#" + slideRGB(RR) + slideRGB(GG) + slideRGB(BB);
        return color;
}

function slideRGB(hexPair) {
	var xx = hexPair;
	xx = 0x80 + parseInt(xx,16);
	xx = (xx.toString(16)).slice(-2);
	return xx
}

